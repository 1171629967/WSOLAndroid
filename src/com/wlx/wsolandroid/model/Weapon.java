package com.wlx.wsolandroid.model;

public class Weapon {
    private String name;
    private int    g;
    private int    p;
    private int    f;
    private int    t;
    private int    w;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getG() {
        return g;
    }

    public void setG(int g) {
        this.g = g;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getF() {
        return f;
    }

    public void setF(int f) {
        this.f = f;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

}
